import random


def jogar():

    mensagem_inicio()

    palavra_secreta = define_palavra_secreta()

    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0
    limite_erros = 7

    while (not enforcou and not acertou):
        chute = chute_jogador()

        if (chute in palavra_secreta):
            preenche_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == limite_erros
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if (acertou):
        ganhou_jogo()
    else:
        perdeu_jogo(palavra_secreta)


def mensagem_inicio():
    print("*********************************")
    print("***Bem vindo ao jogo da Forca!***")
    print("*********************************")


def define_palavra_secreta():
    with open("palavras.txt") as arquivo:
        palavras = []
        for linha in arquivo:
            linha = linha.strip()
            palavras.append(linha)

    numero_palavra = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero_palavra].upper()

    return palavra_secreta


def inicializa_letras_acertadas(palavra):
    letras_acertadas = ["_" for letra in palavra]
    return letras_acertadas


def chute_jogador():
    # Input tratado. Sem espaços (Strip) e Maiusculo (Upper)
    chute = input("Qual letra? ").strip().upper()
    return chute


def preenche_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1


